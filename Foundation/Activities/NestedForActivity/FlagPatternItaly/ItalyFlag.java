package Foundation.Activities.NestedForActivity.FlagPatternItaly;

import java.util.Scanner;

public class ItalyFlag {
    public static void main(String[] args) {
        Scanner reader = new Scanner(System.in);
        int n = reader.nextInt();
        for(int i=1; i<=n; i++)
        {
            for(int j=1; j<=(n/2)*3; j++)
            {
                if((j>=1) && (j<=(n/2)))
                {
                    System.out.print("(");
                }
                else if((j>(n/2)) && (j<=n))
                {
                    System.out.print("#");
                }
                else if((j>n) && (j<=(n/2)*3))
                {
                    System.out.print(")");
                }
            }
            System.out.println();
        }
    }
}
